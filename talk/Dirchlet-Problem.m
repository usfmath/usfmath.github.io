**Title**: On solving the Dirichlet problem

**Speaker**: Louis Arenas

**Date**: October 23rd

**Time**: 11:00 pm

**Abstract**: The talk will start by reviewing the mainstream strategies for solving the problem on simply connected domains, with particular attention to the disk. It is hoped that we have enough time to talk about Malmheden's strategy for solving the problem on the disk. The talk should be of interest to anyone taking or wanting a review of basic concepts in complex analysis.

